package week1.day1;

public class IsPrimeNumber {
	public static void main(String[] args) {
		int number= 13;
		for (int i = 0; i<=number; i++)
		{
		if(i%2==0)
		{
			System.out.println(i+" it is not prime");
			
		}
		else
		{
			System.out.println(i+" it is prime");
		}
	}

	}
}


